var maintain = document.getElementById('maintain')
var brand = document.getElementById('brand')
var model = document.getElementById('model')
brand.innerHTML =`<option>select brand name</option>`
model.innerHTML =`<option>select model name</option>`

function randerall(){

for ( var key in carlist){
    brand.innerHTML+=`<option>${key}</option>`
    for(var key1 in carlist[key]){
        var carvalue = carlist[key][key1]
        colorDiv = '';
        for( i=0; i<carvalue.color.length; i++){
            colorDiv +=`<div class="${carvalue.color[i]}"></div>`
        //    console.log(carvalue.color[i])
        }
 
        maintain.innerHTML+= `
        <div class="col-sm-12 col-md-6 col-lg-3 row1">
            <div class="card cart p-2" style="width: 100%; ">
                <img src=${carvalue.image}
                    class="card-img-top" alt="...">
                <div class="card-body" id='carDetails'>
                <h5 class="card-title">${key1.toLocaleUpperCase()} (${carvalue.model})</h5>
                <p>${carvalue.fuleType}</p>
                <p>${carvalue.Transmission}</p>
                <h5>${carvalue.price}</h5>
                <p>Colors</p>
                <div class="d-flex gap-2 al">${colorDiv}
                </div>

                </div>
            </div>
        </div>`
    }


}
}
randerall()

